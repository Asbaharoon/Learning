package dio.entendendometodosjava;

public class Calculadora {
    protected static double soma (double a, double b){
        return a + b;
    }
    protected static double subtracao (double a, double b){
        return a - b;
    }
    protected static double multiplicacao (double a, double b){
        return a * b;
    }
    protected static double divisao (double a, double b){
        return a / b;
    }


}
